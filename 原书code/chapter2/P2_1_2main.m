syms n                                 %定义符号变量
an=(3*n^2+n)/(2*n^2-1)                 %数列的符号通项
nm=50;                                 %最大整数
tit='二次整数分式';                    %标题的一部分
P2_1_2fun(an,nm,tit)                   %调用函数文件求极限
P2_1_2fun(((-2)^n+3^n)/((-2)^(n+1)+3^(n+1)),20,'指数分式')%调用函数文件求极限
P2_1_2fun(sqrt(n^2+n)-n,200,'根式差') %调用函数文件求极限
P2_1_2fun((1-1/2^(1/n))*cos(n),100,'高次根式复合')%调用函数文件求极限

