P3_20_1fun(2*pi,1-cos(t),'尖点在右的心脏线')%调用函数文件画心脏线和渐屈线
P3_20_1fun(2*pi,1+sin(t),'尖点在下的心脏线')%调用函数文件画心脏线和渐屈线
