syms t                                 %定义符号极角
r=t;                                   %极径的符号表达式
thm=3*pi;                              %最大极角
tit='阿基米德螺线';                    %部分标题
P3_20_1fun(thm,r,tit)                  %调用函数文件画阿基米德螺线和渐屈线
